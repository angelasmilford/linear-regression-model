# Common Mathematical Operations
print(2 + 2) # Addition

print(4 - 2) # Subtraction


# Built-in Math Functions
print(max(2, 4, 6, 11)) # Maximum value from the set

print(min(1954, 1980, 2002, 2004, 2006, 2011)) # Minimum value from the set

print(sqrt(49)) # Square root of a number

print(abs(-6.0)) # Absolute value of a number

print(ceiling(4.1)) # Round up to the nearest integer

print(floor(4.1)) # Round down to the nearest integer